// Combines a compliance-scan submission's report ("Document A") with
// whichever supplementary document applies to that lead ("Document B") into
// a single "Document C", storing all three.
//
// Ported from hccs-master/lib/compliance/generate-document-c.ts (source of
// truth — keep the resolution order in sync if it changes). Used here by
// the admin panel's manual "Regenerate Document C now" action; the primary
// trigger is the Calendly paid-consultation webhook in hccs-master.
//
// Document B resolution order:
//   1. compliance_scan_document_override for this scan (admin-set, per-lead)
//   2. compliance_standard_document (admin-set, global default)
//   3. neither configured — no-op

import 'server-only';
import { mergePdfBuffers } from 'src/lib/compliance/merge-pdf';

const BUCKET = 'compliance-documents';

async function resolveDocumentB(supabase, scanId) {
    const { data: override } = await supabase
        .from('compliance_scan_document_override')
        .select('storage_path, original_filename')
        .eq('compliance_scan_id', scanId)
        .order('created_at', { ascending: false })
        .limit(1)
        .maybeSingle();
    if (override) return override;

    const { data: standard } = await supabase
        .from('compliance_standard_document')
        .select('storage_path, original_filename')
        .order('created_at', { ascending: false })
        .limit(1)
        .maybeSingle();
    return standard ?? null;
}

export async function generateDocumentC(supabase, scanId, documentABuffer, opts = {}) {
    const documentB = await resolveDocumentB(supabase, scanId);
    if (!documentB) {
        return { ok: false, skippedReason: 'no_document_configured' };
    }

    const { data: documentBFile, error: downloadError } = await supabase.storage
        .from(BUCKET)
        .download(documentB.storage_path);
    if (downloadError || !documentBFile) {
        return { ok: false, skippedReason: 'download_failed', message: downloadError?.message };
    }
    const documentBBuffer = Buffer.from(await documentBFile.arrayBuffer());

    const documentCBuffer = await mergePdfBuffers(documentABuffer, documentBBuffer);

    const timestamp = Date.now();
    const basePath = `compliance/${scanId}/${timestamp}`;
    const documentAPath = `${basePath}-document-a.pdf`;
    const documentCPath = `${basePath}-document-c.pdf`;

    const [uploadA, uploadC] = await Promise.all([
        supabase.storage.from(BUCKET).upload(documentAPath, documentABuffer, { contentType: 'application/pdf' }),
        supabase.storage.from(BUCKET).upload(documentCPath, documentCBuffer, { contentType: 'application/pdf' }),
    ]);
    if (uploadA.error || uploadC.error) {
        return { ok: false, skippedReason: 'upload_failed', message: uploadA.error?.message || uploadC.error?.message };
    }

    const { data: createdRow, error: insertError } = await supabase
        .from('compliance_document')
        .insert({
            compliance_scan_id: scanId,
            uploaded_by: opts.uploadedBy || null,
            document_b_filename: documentB.original_filename,
            document_b_path: documentB.storage_path,
            document_a_path: documentAPath,
            document_c_path: documentCPath,
            notes: opts.notes ?? 'Manually regenerated by staff from the admin panel.',
        })
        .select('id, created_at')
        .single();

    if (insertError || !createdRow) {
        return { ok: false, skippedReason: 'insert_failed', message: insertError?.message };
    }

    return { ok: true, data: { id: createdRow.id, created_at: createdRow.created_at, document_a_path: documentAPath, document_b_path: documentB.storage_path, document_c_path: documentCPath } };
}
